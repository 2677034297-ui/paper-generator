// ============================================================
// 4. 升档改写 Prompt
// ============================================================
export function getUpgradePrompt(targetBand: "11" | "14"): string {
  const bandRequirements =
    targetBand === "14"
      ? `## 14分档（最高档）要求
- 完全切题，论点深刻有说服力
- 内容充实，论证充分
- 结构严谨，逻辑清晰，段落衔接自然流畅
- 语法基本无错误，句式丰富多变
- 词汇丰富精准，能灵活使用高级词汇和复杂句式
- 无中式英语，表达地道自然`
      : `## 11分档要求
- 切题，论点明确
- 内容较完整，有一定说服力
- 结构清晰，有基本的逻辑连接
- 语法错误较少，不影响理解
- 词汇有一定变化，尝试使用复杂句式
- 避免明显的中式表达`;

  return `你是一位四六级作文阅卷老师。请帮助学生将作文升级到${targetBand}分档水平。

## 改写原则
1. **保留原意**：不改变学生的核心观点和论点
2. **高级化表达**：用更正式、更地道的书面表达替换口语化表达
3. **优化句式**：长短句结合，增加从句、倒装等高级句式
4. **增强逻辑**：添加适当的连接词，使段落衔接更自然
5. **消除中式英语**：把中式表达改为地道的英文表达
6. **丰富词汇**：用更精准、更高级的词汇替换普通词汇

${bandRequirements}

## 输出格式（必须严格JSON）
{
  "improved_version": "完整改写后的作文",
  "changes_summary": "改动说明，列出主要改进点，100-150字"
}

## 注意事项
- 改写要自然，不要生硬堆砌高级词汇
- 保持文章长度与原作相当
- 不要添加原文中没有的论点
- 确保改写后的文章读起来像人写的，不是机器生成的`;
}

// ============================================================
// 5. 构建完整的用户消息
// ============================================================
export function buildScoringMessage(essay: string, topic?: string): string {
  const topicLine = topic
    ? `作文题目：${topic}\n\n`
    : "请根据作文内容判断是否切题。\n\n";
  return `${topicLine}学生作文：\n\n${essay}`;
}

export function buildGrammarMessage(essay: string): string {
  return `请仔细检查以下学生作文的语法错误和可升级的表达：\n\n${essay}`;
}

export function buildUpgradeMessage(
  essay: string,
  targetBand: "11" | "14"
): string {
  return `请将以下学生作文升级到${targetBand}分档水平：\n\n${essay}`;
}
